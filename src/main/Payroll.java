      public void insertPayroll(){
           try(Connection con = DriverManager.getConnection(url, user, password)){
                    String query = "INSERT INTO allowance (serialNo,name,workingType,rank,month,bSalary,type,hour,reason,amount,totalSalary,medical,bonus,other) VALUES (?, ?, ?, ?,?,?,?, ?, ?, ?,?,?,?,?)";
 
                     pst = con.prepareStatement(query);
                     pst.setString(1, TFserialNo.getText());
                     pst.setString(2, TFname.getText());
                     pst.setString(3, TFworkingType.getText());
                     pst.setString(4, TFrank.getText());
                     //int no=Integer.parseInt(TFcontactNo.getText());
                     String month=CBmonth.getSelectedItem().toString();
                     pst.setString(5, month);
                     pst.setString(6, TFsalary.getText());
                     String type=CBmonth.getSelectedItem().toString();
                     pst.setString(7,type);
                  
                     pst.setString(8, TFhour.getText());
                     String reason=CBmonth.getSelectedItem().toString();
                     pst.setString(9, reason);
                     pst.setString(10, TFamount.getText());
                     pst.setString(11, TFtotalSalary.getText());
                     pst.setString(12, TFmedical.getText());
                     pst.setString(13, TFbonus.getText());
                     pst.setString(14, TFother.getText());
                     
                     int rowsInserted = pst.executeUpdate();
                     if (rowsInserted > 0) {
                     JOptionPane.showMessageDialog(null,"Payroll is inserted successfully!");
                 }
           
        } catch (SQLException ex) {
            Logger.getLogger(Payroll.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
