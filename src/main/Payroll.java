    public void advanceSearch(String query){
         try(Connection con = DriverManager.getConnection(url, user, password)){
          
                         
               pst=con.prepareStatement(query);
               pst.setString(1, TFsearch.getText());
               rs=pst.executeQuery();
               if(rs.next()){
                   String serialNo=rs.getString("serialNo");
                   TFserialNo.setText(serialNo);
               
                   
                  String name = rs.getString("name");
                   TFname.setText(name);
                   
                   String address = rs.getString("address");
                   TFaddress.setText(address);
                   
                   String contactNo = rs.getString("contactNo");
                   TFsalary.setText(contactNo);
                   
                   //RADIO BUTTON
                   
                   String gender = rs.getString("gender");
                   if(gender=="Male"){
                       RBmale.setSelected(true);
                       RBfemale.setSelected(false);
                   }
                   else{
                       RBfemale.setSelected(true);
                       RBmale.setSelected(false);
                   }
                   
                   //COMBO BOX
                   String workingType=rs.getString("workingType");
                   if((workingType)=="Full Time"){
                       CBworkingType.setSelectedItem(workingType);
                   }
                   else
                   {
                       CBworkingType.setSelectedItem("Part Time");
                   }
                   String date = rs.getString("entryDate");
                   
                   
                   ((JTextField)JentryDate.getDateEditor().getUiComponent()).setText(date);
                   
                   String rank = rs.getString("rank");
                   
                   switch(rank){  
                       case "Cutting":
                           
                           CBtype.setSelectedItem("Cutting");
                           break;
                       case "Sewing":
                           CBtype.setSelectedItem("Sewing");
                           break;
                       case "Maintenance":
                           CBtype.setSelectedItem("Maintenance");
                           break;
                        case "Finishing":
                           CBtype.setSelectedItem("Finishing");
                           break;
                        case "Accounts Officer":
                           CBtype.setSelectedItem("Accounts Officer");
                           break;
                        case "Manager":
                           CBtype.setSelectedItem("Manager");
                           break;
                        case "Other":
                           CBtype.setSelectedItem("Other");
                           break;
                        default:
                            CBtype.setSelectedItem("Cutting");
                           
                           
                   }
                   //int salary=Integer.valueOf(rs.getString("bSalary"));
                   String salary = rs.getString("bSalary");
                   TFhour.setText(salary);
                   byte[] img=rs.getBytes("image");
                   ImageIcon icon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH));
                   imageLabel.setIcon(icon);
                   
              
              }
              
           
       } catch (SQLException ex) {
            Logger.getLogger(Vendor.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
    }
